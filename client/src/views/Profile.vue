              }
            }
          }
        },
        plugins: {
          trigger: new window.FormValidation.plugins.Trigger(),
          bootstrap: new window.FormValidation.plugins.Bootstrap(),
          submitButton: new window.FormValidation.plugins.SubmitButton()
        }
      })
    },
    accountFormValidation () {
