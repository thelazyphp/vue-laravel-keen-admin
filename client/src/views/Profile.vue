              },
              stringLength: {
                max: 191,
                message: "Имя пользователя не может превышать 191 символ"
              }
            }
          },
          cur_password: {
            validators: {
              notEmpty: {
                message: "Введите текущий пароль"
              }
            }
          },
          new_password: {
            validators: {
              stringLength: {
                min: 8,
